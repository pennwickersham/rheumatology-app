export function getMedicationById(id) {
  return medications.find(m => m.id === id);
}

export function getMedicationsByClass(classId) {
  return medications.filter(m => m.drugClass === classId);
}

export function searchMedications(query) {
  const q = query.toLowerCase();
  return medications.filter(m =>
    m.genericName.toLowerCase().includes(q) ||
    m.brandNames.some(b => b.toLowerCase().includes(q)) ||
    m.drugClass.toLowerCase().includes(q)
  );
}

export function getDrugClassById(id) {
  return drugClasses.find(c => c.id === id);
}
